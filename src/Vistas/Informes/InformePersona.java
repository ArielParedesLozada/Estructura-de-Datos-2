    private void setTable() {
        this.tblModel = new DefaultTableModel();
        this.tblModel.addColumn("Destino");
        this.tblModel.addColumn("Horario");
        this.tblModel.addColumn("Bus");
        this.jTblPersona.setModel(this.tblModel);
    }

    private void setTable(String id) {
        for (Salida salida : this.gestor.registro.getValues(id)) {
            this.tblModel.addRow(new Object[]{salida.destino, salida.horario.toString(), salida.getIDBus()});
        }
        this.jTblPersona.setModel(this.tblModel);
    }

