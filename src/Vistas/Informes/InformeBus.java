    private void jBtnBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBusActionPerformed
        String id = this.jTxtBusID.getText();
        try {
            float ocupacion = this.gestor.buses.find(id).promedio();
            this.jLblPorcentaje.setText(Float.toString(ocupacion)+" %");
        }catch(ArithmeticException ae){
            JOptionPane.showMessageDialog(null, "Fallo al calcular el promedio");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se encuentra");
        }
    }//GEN-LAST:event_jBtnBusActionPerformed

